/*Self explanatory. This one deals with:
    Flow control - Switch Case
*/

var dayofWeek = Sun;

switch (dayofWeek)
{
    case "Sun":
        alert("Whopeee");
        break;

    case "Sat":
        alert("Whopee");
        break;

    case "Fri":
        alert("TGIF");
        break;

    default:
        alert("Shoot me now");
        break;
}